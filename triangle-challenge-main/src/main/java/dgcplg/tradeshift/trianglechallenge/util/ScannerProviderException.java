package dgcplg.tradeshift.trianglechallenge.util;

public class ScannerProviderException extends Exception {
	private static final long serialVersionUID = -734616089879989894L;

	public ScannerProviderException() {
		super();
	}
	
	public ScannerProviderException(String message) {
		super(message);
	}
	
	public ScannerProviderException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public ScannerProviderException(Throwable cause) {
		super(cause);
	}
}
